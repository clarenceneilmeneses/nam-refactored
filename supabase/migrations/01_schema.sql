CREATE INDEX idx_sales_date       ON sales (date);
CREATE INDEX idx_sales_company    ON sales (company);
CREATE INDEX idx_sales_category   ON sales (category);
CREATE INDEX idx_sales_created_at ON sales (created_at);

-- ---------- Audit ----------
CREATE TABLE system_logs (
  id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id     integer NOT NULL,   -- intentionally no FK: legacy logs reference deleted users
  action      text NOT NULL,
  description text,
  ip_address  text,
  created_at  timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX idx_system_logs_user_id ON system_logs (user_id);

-- ---------- Row Level Security ----------
-- Enable RLS on every table. Until you write real policies (see the
-- migration guide), server-side access via the service role key still works,
-- and the anon key is locked out — which is what you want by default.
